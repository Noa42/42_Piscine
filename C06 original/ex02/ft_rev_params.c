#include <unistd.h>

void	ft_putstr(char *str)
{
	int		i;
	char	letra;

	i = 0;
	while (str[i] != '\0')
	{
		letra = str[i];
		write (1, &letra, 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	i;

	i = argc - 1;
	while (i > 0)
	{
		ft_putstr(argv[i]);
		write (1, "\n", 1);
		i--;
	}
}
